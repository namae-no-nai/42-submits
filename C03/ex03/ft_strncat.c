char	*ft_strncat(char *dest, char *src, unsigned int nb)
{
	char	*a;

	a = dest;
	while (*a != '\0')
		a++;
	while (nb > 0 && *src != '\0')
	{
		*a = *src;
		src++;
		a++;
		nb--;
	}
	*a = '\0';
	return (dest);
}
